// JavaScript math object provides several constants and methods to perform mathematical operation. 
// Unlike date object, it doesn't have constructors.

let a = 5;
let b = 10;

console.log("Absolute value of the number: ",Math.abs(a));
console.log("Maximum value among the numbers: ", Math.max(a,b));
console.log("Minimum value is: ",Math.min(a,b));
console.log("Square root: ", Math.sqrt(a));
console.log("Power of exponent: ", Math.pow(2,5));
